"use strict";
let age = 0;
if (age == 0 && age < 2) {
    console.log(`Baby`);
}
else
    (age == 2 && age < 4);
{
    console.log(`Toddler`);
}
if (age == 4 && age < 13) {
    console.log(`Kid`);
}
else
    (age == 13 && age < 20);
{
    console.log(`Teenage`);
}
if (age == 20 && age < 65) {
    console.log(`Adult`);
}
else
    (age == 65 && age > 65);
{
    console.log(`Elder`);
}
